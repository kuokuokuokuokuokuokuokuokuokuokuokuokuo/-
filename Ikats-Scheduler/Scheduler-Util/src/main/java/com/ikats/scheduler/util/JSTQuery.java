package com.ikats.scheduler.util;


/**
 * 聚水潭使用奇门接口所需
 */
public class JSTQuery extends JSTOpenApi {
	  public JSTQuery(String sAppKey, String sAppSecret, String sPartnerId, String sPartnerKey, String stoken , String sRequestName, String sHostUrl)
	  {
		  this.partnerid=sPartnerId;
		  this.partnerkey=sPartnerKey;
		  this.method=sRequestName;
		  this.HostUrl=sHostUrl;
		  this.token=stoken;
		  this.app_key=sAppKey;
		  this.Secret=sAppSecret;
	  }
}